public class RssChannel {
	private String rss_title = "";
	private String rss_link = "";
	private String rss_description = "";
	private String rss_language = "";
	private String rss_ttl = "";
	

	public String getRss_title() {
		return rss_title;
	}

	public void setRss_title(String rss_title) {
		this.rss_title = rss_title;
	}

	public String getRss_link() {
		return rss_link;
	}

	public void setRss_link(String rss_link) {
		this.rss_link = rss_link;
	}

	public String getRss_description() {
		return rss_description;
	}

	public void setRss_description(String rss_description) {
		this.rss_description = rss_description;
	}

	public String getRss_language() {
		return rss_language;
	}

	public void setRss_language(String rss_language) {
		this.rss_language = rss_language;
	}

	public String getRss_ttl() {
		return rss_ttl;
	}

	public void setRss_ttl(String rss_ttl) {
		this.rss_ttl = rss_ttl;
	}
}
